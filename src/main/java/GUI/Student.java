    private void validateRegistrationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validateRegistrationButtonActionPerformed
        String name = nameTextField.getText();
        String surname = surnameTextField.getText();
        String id = idNumberTextField.getText();
        LocalDate dob = dobDatePicker.getDate();
        int grade = (int) gradeSpinner.getValue();

        //Presence check for name
        if (!validator.presenceCheck(name)) {
            isNameValid = false;
            nameTextField.setBackground(Color.red);
            nameErrorMessageLabel.setForeground(Color.red);
            nameErrorMessageLabel.setText("Please enter a name");
        } else {
            isNameValid = true;
            nameErrorMessageLabel.setText("");
        }

        //Presence check for surname
        if (!validator.presenceCheck(surname)) {
            surnameTextField.setBackground(Color.red);
            surnameErrorMessageLabel.setForeground(Color.red);
            surnameErrorMessageLabel.setText("Please enter a surname");
        } else {
            isSurnameValid = true;
            surnameErrorMessageLabel.setText("");
        }

        //Presence check for id
        if (!validator.presenceCheck(id)) {
            isIdValid = false;
            idNumberTextField.setBackground(Color.red);
            idNumberErrorMessageLabel.setForeground(Color.red);
            idNumberErrorMessageLabel.setText("Please enter an ID Number");
        } else {
            isIdValid = true;
            idNumberErrorMessageLabel.setText("");
        }

        //Handles ID and dob error
        if (!validator.checkIDandDOB(id, dob)) {
            dobDatePicker.setBackground(Color.red);
            dobErrorMessageLabel.setForeground(Color.red);
            dobErrorMessageLabel.setText("Your ID and date of birth do not correspond");
            isNameValid = false;
        } else {
            dobDatePicker.setBackground(Color.white);
            dobErrorMessageLabel.setText("");
            isNameValid = true;
        }

        //Handles age and grade error
        if (!validator.ageGradeLogicCheck(dob, grade)) {
            dobDatePicker.setBackground(Color.red);
            gradeErrorMessageLabel.setForeground(Color.red);
            gradeErrorMessageLabel.setText("Your age does not correspond with your current grade.");
        } else {
            dobDatePicker.setBackground(Color.white);
            gradeErrorMessageLabel.setText("");
        }

        //Handles radio button errors
        if (!yesRadioButton.isSelected() && !noRadioButton.isSelected()) {
            schoolEmailErrorMessageLabel.setForeground(Color.red);
            schoolEmailErrorMessageLabel.setText("Please select either yes or no");
            isEmailValid = false;
        } else {
            schoolEmailErrorMessageLabel.setText("");
            isEmailValid = true;
        }

        if (isNameValid && isSurnameValid && isIdValid && isIdValid && isEmailValid
                && validator.ageGradeLogicCheck(dob, grade)
                && validator.checkIDandDOB(id, dob)) {

            JOptionPane.showMessageDialog(null, "Your registration was successful", "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_validateRegistrationButtonActionPerformed

