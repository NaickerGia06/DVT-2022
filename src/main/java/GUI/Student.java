    private void idNumberTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idNumberTextFieldKeyReleased
        String id = idNumberTextField.getText();

        if (!validator.presenceCheck(id)) {
            idNumberTextField.setBackground(Color.red);
            idNumberErrorMessageLabel.setForeground(Color.red);
            idNumberErrorMessageLabel.setText("Please enter an ID Number");
            isIdValid = false;
        } else if (!validator.checkDigit(id)) {
            idNumberTextField.setBackground(Color.red);
            idNumberErrorMessageLabel.setForeground(Color.red);
            idNumberErrorMessageLabel.setText("ID cannot contain special characters");
            isIdValid = false;
        } else if (!validator.checkLength(id)) {
            idNumberTextField.setBackground(Color.red);
            idNumberErrorMessageLabel.setForeground(Color.red);
            idNumberErrorMessageLabel.setText("The ID must be 13 digits");
            isIdValid = false;
        } else {
            idNumberTextField.setBackground(Color.white);
            idNumberErrorMessageLabel.setText("");
            isIdValid = true;
        }
    }//GEN-LAST:event_idNumberTextFieldKeyReleased

