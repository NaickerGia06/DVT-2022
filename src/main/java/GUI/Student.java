    private void surnameTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_surnameTextFieldKeyReleased
        String surname = surnameTextField.getText();

        if (!validator.presenceCheck(surname)) {
            surnameTextField.setBackground(Color.red);
            surnameErrorMessageLabel.setForeground(Color.red);
            isSurnameValid = false;
            surnameErrorMessageLabel.setText("Please enter a surname");
        } else if (!validator.checkString(surname)) {
            surnameTextField.setBackground(Color.red);
            surnameErrorMessageLabel.setForeground(Color.red);
            surnameErrorMessageLabel.setText("Surname cannot contain digits/characters");
            isSurnameValid = false;
        } else {
            surnameTextField.setBackground(Color.white);
            surnameErrorMessageLabel.setText("");
            isSurnameValid = true;

        }
    }//GEN-LAST:event_surnameTextFieldKeyReleased

